package model;

public class ProductKeyword {
	
	int productID;
	int keywordID;
	
	
	
	public ProductKeyword(int productID, int keywordID) {
		super();
		this.productID = productID;
		this.keywordID = keywordID;
	}
	public int getProductID() {
		return productID;
	}
	public int getKeywordID() {
		return keywordID;
	}
	
	
	
	

}
